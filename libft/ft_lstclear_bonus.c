#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*node;
	t_list	*tmp;

	if (!lst || !del)
		return ;
	node = *lst;
	while (node)
	{
		tmp = node -> next;
		del(node -> content);
		free(node);
		node = tmp;
	}
	*lst = NULL;
}
